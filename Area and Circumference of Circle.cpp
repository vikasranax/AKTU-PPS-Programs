//C++ Program to calculate area and circumference of a circle.



#include<bits/stdc++.h>
using namespace std; 
int main()
{
    double radius;
    cout<<"Enter the Radius of Circle in Centimeter\n";
    cin>>radius;
    
    double circumference = 2*3.14*radius;
    cout<<"Circumference is :"<<circumference<<"cm"<<endl;
    
    
    double area = 3.14*radius*radius;
    cout<<"Area is :"<<area<<"cm^2";
    return 0;
}
