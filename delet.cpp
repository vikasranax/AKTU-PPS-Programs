// C++ Program to check numbers are equal or not.

#include <bits/stdc++.h>
using namespace std;
int main()
{
    double a, b;
    cout << "Enter a number : \n";
    cin >> a;
    cout << "Again Enter a number : \n";
    cin >> b;
    if (a == b)
    {
        cout << "Numbers are equal.";
    }
    else
        cout << "Numbers are not Equal.";

    return 0;
}
